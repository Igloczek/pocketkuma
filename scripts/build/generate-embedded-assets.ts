#!/usr/bin/env bun
import fs from "fs";
import path from "path";

const projectRoot = path.resolve(import.meta.dirname, "../..");
const distDir = path.join(projectRoot, "dist");
const outputDir = path.join(projectRoot, "src/server/generated");
const outputFile = path.join(outputDir, "embedded-assets.bundle.ts");

function walkFiles(dir: string, root = dir): string[] {
    const entries = fs.readdirSync(dir, { withFileTypes: true });
    const files: string[] = [];

    for (const entry of entries) {
        const fullPath = path.join(dir, entry.name);
        if (entry.isDirectory()) {
            files.push(...walkFiles(fullPath, root));
            continue;
        }

        files.push(path.relative(root, fullPath).split(path.sep).join("/"));
    }

    return files.sort();
}

function toImportId(index: number) {
    return `asset_${index}`;
}

function toImportPath(relativePath: string) {
    return `../../../dist/${relativePath.split("/").join("/")}`;
}

if (!fs.existsSync(distDir)) {
    console.error("dist/ not found. Run `bun run build:frontend` first.");
    process.exit(1);
}

const webAssets = walkFiles(distDir);
if (webAssets.length === 0) {
    console.error("dist/ is empty. Run `bun run build:frontend` first.");
    process.exit(1);
}

const importLines: string[] = [];
const mapEntries: string[] = [];

for (let i = 0; i < webAssets.length; i++) {
    const webPath = webAssets[i];
    const importId = toImportId(i);
    importLines.push(`import ${importId} from "${toImportPath(webPath)}" with { type: "file" };`);
    mapEntries.push(`    "${webPath}": ${importId},`);
}

fs.mkdirSync(outputDir, { recursive: true });

const source = `// @ts-nocheck
// AUTO-GENERATED by scripts/build/generate-embedded-assets.ts — do not edit.

${importLines.join("\n")}

const embeddedWebAssets = {
${mapEntries.join("\n")}
};

function hasEmbeddedAsset(webPath) {
    return Object.prototype.hasOwnProperty.call(embeddedWebAssets, webPath);
}

function getEmbeddedAssetRef(webPath) {
    return embeddedWebAssets[webPath];
}

async function readEmbeddedAsset(webPath) {
    const ref = embeddedWebAssets[webPath];
    if (!ref) {
        return null;
    }

    const file = Bun.file(ref);
    if (!(await file.exists())) {
        return null;
    }

    return file;
}

async function readEmbeddedAssetText(webPath) {
    const file = await readEmbeddedAsset(webPath);
    if (!file) {
        return null;
    }

    return file.text();
}

export { embeddedWebAssets, hasEmbeddedAsset, getEmbeddedAssetRef, readEmbeddedAsset, readEmbeddedAssetText };
`;

fs.writeFileSync(outputFile, source);
console.log(`Generated ${outputFile} with ${webAssets.length} embedded web assets.`);
